package kampOdevUcuncuGun;

public class Student extends User {
	private String firstName;
	private String lastName;
	private String joinedCourseName;
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getJoinedCourseName() {
		return joinedCourseName;
	}
	public void setJoinedCourseName(String joinedCourseName) {
		this.joinedCourseName = joinedCourseName;
	}
	
	
}
